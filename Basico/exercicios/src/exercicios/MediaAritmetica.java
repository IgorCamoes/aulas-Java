package exercicios;

public class MediaAritmetica {
	
	public static void main(String[] args) {
		
		double medias1 = (8 + 9 + 7) / 3;
		
		double medias2 = (4 + 5 + 6) / 3;
		
		System.out.println("A média de 7, 8 e 9 é " + medias1);
		
		System.out.println("A média de 4, 5 e 6 é " + medias2);
		
		System.out.println("A soma das duas médias é " + (medias1+medias2));
		
		System.out.println("A média dessas duas médias é " + ((medias1+medias2)/2));
		
	}

}
